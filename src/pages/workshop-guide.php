    <h1 itemprop="name">Steam Workshop Guide</h1>
    <h2 itemprop="description">How to Create and Manage Steam Workshop Collections</h2>
    
    <div class="card">
        <div itemprop="step" itemscope itemtype="https://schema.org/HowToStep">
            <h3 itemprop="name">Creating a Collection</h3>
            <div itemprop="text">
                <ol>
                    <li>Open Steam and log into your account</li>
                    <li>Navigate to the Steam Workshop</li>
                    <li>Click on your profile name</li>
                    <li>Select "Collections" from the dropdown</li>
                    <li>Click "Create Collection"</li>
                    <li>Give your collection a name and description</li>
                    <li>Choose visibility (public/private)</li>
                    <li>Click "Save Collection"</li>
                </ol>
            </div>
        </div>
    </div>

    <div class="card">
        <div itemprop="step" itemscope itemtype="https://schema.org/HowToStep">
            <h3 itemprop="name">Adding Mods to Your Collection</h3>
            <div itemprop="text">
                <ol>
                    <li>Browse the Workshop for mods you want</li>
                    <li>On each mod page, click "Add to Collection"</li>
                    <li>Select your collection from the dropdown</li>
                    <li>Optionally, rearrange mods in your collection</li>
                    <li>Save changes to update your collection</li>
                </ol>
            </div>
        </div>
    </div>

    <div class="card">
        <h3>Best Practices for Collection Management</h3>
        <ul>
            <li>Create separate collections for different mod categories or purposes to keep your content organized and easy to manage</li>
            <li>Regularly back up your collection URLs to prevent loss of carefully curated mod lists</li>
            <li>Test your mod combinations thoroughly before sharing collections with other users</li>
            <li>Consider load order when arranging mods to ensure compatibility and optimal performance</li>
        </ul>
    </div>
